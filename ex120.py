fruit = {"봄" : "딸기", "여름" : "토마토", "가을" : "사과"}
user = input("좋아하는 과일은?")
if user in fruit.values():
    print("정답입니다.")
else:
    print("오답입니다.")
#과일이름으로 이루어져 있는 딕셔너리를 만들고 input으로 실행시킬 때 좋아하는 과일을 이라는 질문을 던진다
#딸기 토마토 사과를 답하면 정답입니다. 그 외의 다른 단어를 쓰면 오답입니다. 가 출력된다. 예를들어 한라봉을 넣으면 오답입니다. 가 출력된다


